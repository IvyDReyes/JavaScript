//To check if a number is less than another number, we use the less-than operator, <.
console.log(1 < 90);

//If the number on the leftis less than the number on the right, the result is true
console.log(1 < 235);

//If the number on the left isn't less than the number on the right, the result is false
console.log(235 < 1);


//To check if the number is greater than another number, we use the greater-than operator, >.
console.log(101 > 90)

