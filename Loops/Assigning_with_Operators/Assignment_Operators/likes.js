//Add from a variable's value
let likes = 5;
likes += 1;
console.log(likes);

//Subtract from a variable's value
let likes1 = 5;
likes1 -= 3;
console.log(likes1);

//Increment operator ++, placed after a variables name, increases its value by 1.
let likes2 = 5;
likes2++;
console.log(likes2);

//Decrement operator --, placed after a variables name, decreases its value by 1.
let likes3 = 5;
likes3--;
console.log(likes3);